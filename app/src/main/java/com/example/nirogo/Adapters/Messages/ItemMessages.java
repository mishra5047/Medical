package com.example.nirogo.Adapters.Messages;

public class ItemMessages {
    private int imageUser, imageMessage;
    private String nameUser, textUser;

    public int getImageUser() {
        return imageUser;
    }

    public void setImageUser(int imageUser) {
        this.imageUser = imageUser;
    }

    public int getImageMessage() {
        return imageMessage;
    }

    public void setImageMessage(int imageMessage) {
        this.imageMessage = imageMessage;
    }

    public String getNameUser() {
        return nameUser;
    }

    public void setNameUser(String nameUser) {
        this.nameUser = nameUser;
    }

    public String getTextUser() {
        return textUser;
    }

    public void setTextUser(String textUser) {
        this.textUser = textUser;
    }
}
